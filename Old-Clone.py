def get_device_id():
    """
    Multi-source fingerprint; stable on the same device, Termux-safe.
    """
    parts = []

    # 1) Termux-API (if available)
    j = sh("termux-telephony-deviceinfo || true")
    if j:
        parts.append(j)

    # 2) ANDROID_ID (may work without root on some devices)
    aid = sh("settings get secure android_id || true")
    if aid and aid.lower() != "null":
        parts.append("AID:"+aid)

    # 3) ro props
    for p in ["ro.product.manufacturer", "ro.product.model", "ro.serialno", "ro.build.fingerprint"]:
        val = sh(f"getprop {p} || true")
        if val:
            parts.append(f"{p}:{val}")

    # 4) MAC/hostname fallbacks
    try:
        parts.append("NODE:"+str(uuid.getnode()))
    except Exception:
        parts.append("NODE:0")
    try:
        parts.append("UNAME:"+str(os.uname()))
    except Exception:
        parts.append("UNAME:unknown")

    raw = "|".join(parts)
    if not raw.strip():
        raw = "fallback-"+str(uuid.uuid4())
    # Stable device_id (do not expose SALT here)
    device_id = hashlib.sha256(raw.encode()).hexdigest()
    return device_id

